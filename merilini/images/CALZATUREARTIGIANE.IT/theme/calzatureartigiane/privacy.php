<p><strong>Come posso disattivare i cookies ?</strong></p>
<p>È possibile modificare il browser per disattivare i cookies tramite una procedura molto semplice. Attenzione: disattivando i cookies sia nome utente che password non saranno più memorizzati sul box di login del sito.</p>
<p><br />
	<strong>Firefox:</strong></p>
<p><br />
	1. Apri Firefox<br />
	2. Premi il pulsante “Alt” sulla tastiera<br />
	3. Nella barra degli strumenti situata nella parte superiore del browser, seleziona “Strumenti” e successivamente “Opzioni”<br />
	4. Seleziona quindi la scheda “Privacy”<br />
	5. Vai su “Impostazioni Cronologia:” e successivamente su “Utilizza impostazioni personalizzate” . Deseleziona “Accetta i cookie dai siti” e salva le preferenze.</p>
<p><br />
	<strong>Internet Explorer:</strong></p>
<p><br />
	1. Apri Internet Explorer<br />
	2. Clicca sul pulsante “Strumenti” e quindi su “Opzioni Internet”<br />
	3. Seleziona la scheda “Privacy” e sposta il dispositivo di scorrimento sul livello di privacy che desideri impostare (verso l’alto per bloccare tutti i cookies o verso il basso per consentirli tutti)<br />
